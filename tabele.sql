DROP DATABASE IF EXISTS Platforma_Recenzii;
CREATE DATABASE Platforma_Recenzii;

USE Platforma_Recenzii;

CREATE TABLE tblUtilizator(
    idUtilizator SMALLINT PRIMARY KEY AUTO_INCREMENT,
    nume VARCHAR(50),
    prenume VARCHAR(50),
    email VARCHAR(125),
    parola VARCHAR(50)
);

CREATE TABLE tblFilme_Seriale(
    idFS SMALLINT PRIMARY KEY,
    titlu VARCHAR(30) UNIQUE,
    an_lansare SMALLINT,
    gen VARCHAR(30),
    platforma_streaming VARCHAR(50),
    tip ENUM ('Film', 'Serial')
);

CREATE TABLE tblWatchlist(
	codUtilizator SMALLINT,
	codFS SMALLINT,
	FOREIGN KEY (codUtilizator) REFERENCES tblUtilizator(idUtilizator),
    FOREIGN KEY (codFS) REFERENCES tblFilme_Seriale(idFS)
);

CREATE TABLE tblRecenzie(
    idRecenzie SMALLINT PRIMARY KEY AUTO_INCREMENT,
    idUtilizator SMALLINT,
    idFS SMALLINT,
    FOREIGN KEY (idFS) REFERENCES tblFilme_Seriale(idFS),
    FOREIGN KEY (idUtilizator) REFERENCES tblUtilizator(idUtilizator),
    rating SMALLINT,
    comentariu VARCHAR(300),
    data_recenzie DATE
);

CREATE TABLE tblFilm(
    idFS SMALLINT PRIMARY KEY,
    durata_min INT,
    FOREIGN KEY (idFS) REFERENCES tblFilme_Seriale(idFS)
);

CREATE TABLE tblSerial(
    idFS SMALLINT PRIMARY KEY,
    nr_sezoane SMALLINT,
    FOREIGN KEY (idFS) REFERENCES tblFilme_Seriale(idFS)
);

